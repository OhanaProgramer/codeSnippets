"""
Explanation:
This program demonstrates how to use an if-else statement to check if a given age falls within a specific range.
The logical `and` operator is used to ensure that both conditions are satisfied.
"""

#Assign age to a variable
age = 25

#check if age is between 18 and 30
if 18 <= age <= 30:
    print("Age is between 18 and 30")
else:
    print("Age is not between 18 and 30")

#Output: 
# Age is between 18 and 30