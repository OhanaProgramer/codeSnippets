#include <iostream>
using namespace std;

int main() {
    int age = 18;
    if (age >= 18 && age <= 30) {
        cout << "You are between 18 and 30 years old." << endl;
    } else {
        cout << "You are not between 18 and 30 years old." << endl;
    }
    return 0;
}

// Output:
// You are between 18 and 30 years old.