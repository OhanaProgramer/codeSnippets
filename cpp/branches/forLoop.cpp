/* Explanation:
for loop has three parts: initialization (int i = 0), condition (i < 5), and increment (i++).
The loop runs while i is less than 5.
*/

#include <iostream>
using namespace std;

int main() {
    for (int i=0; i< 5; i++) {
        cout << "Iteration is: " << i << endl;
    }

    return 0;
}