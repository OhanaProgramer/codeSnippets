#include <iostream>
using namespace std;

int main() {
    int age = 18;
    if (age == 18) {
        cout << "You are 18 years old." << endl;
    } else {
        cout << "You are not 18 years old." << endl;
    }
    return 0;
}

// Output:
// You are 18 years old.